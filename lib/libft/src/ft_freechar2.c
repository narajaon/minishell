#include "libft.h"

void	ft_freechar2(char **cmd)
{
	int i;

	i = 0;
	while (cmd[i] != NULL)
	{
		free(cmd[i]);
		i++;
	}
	if (cmd)
	{
		free(cmd);
		cmd = NULL;
	}
}
